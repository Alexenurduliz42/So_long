#include "libft.h"

void	*ft_memchr(const void *s, int c, int n)
{
	char	*str;
	int		i;

	i = 0;
	str = (char *)s;
	while (i < n)
	{
		if ((unsigned char)str[i] == (unsigned char)c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
